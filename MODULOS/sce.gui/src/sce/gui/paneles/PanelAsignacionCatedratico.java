    private void catedraticoTablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_catedraticoTablaMouseClicked
        if (evt.getClickCount() > 1){
            filtroTabla = new TableRowSorter(catedraticoTabla.getModel());
            nombreTxt.setText((String)modelInformacion.getValueAt(filtroTabla.convertRowIndexToModel(catedraticoTabla.getSelectedRow()), 0));
            apellidoTxt.setText((String)modelInformacion.getValueAt(filtroTabla.convertRowIndexToModel(catedraticoTabla.getSelectedRow()), 1));
            dpiTxt.setText((String)modelInformacion.getValueAt(filtroTabla.convertRowIndexToModel(catedraticoTabla.getSelectedRow()), 2));
        }
    }//GEN-LAST:event_catedraticoTablaMouseClicked


