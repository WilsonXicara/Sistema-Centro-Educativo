    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        AsignacionCatedratico asignador = new AsignacionCatedratico(emf);
        BuscadorCatedratico buscador = new BuscadorCatedratico(emf);
        AsignacionCarreraBuscador buscadorCarrera = new AsignacionCarreraBuscador(emf);
        CarreraEntity cat = (CarreraEntity) modeloCombo.getSelectedItem();
        asignador.setIdAsignacionCarrera(buscadorCarrera.obtenerAsignacionCarrera(cat.getId()).getId());
        asignador.setIdCatedratico(buscador.obtenerCatedratico(dpiTxt.getText()).getId());
        try {
            asignador.crearAsignacion();
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(PanelAsignacionCatedratico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

